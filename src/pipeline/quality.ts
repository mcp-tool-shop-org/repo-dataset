/** Quality filters — token bounds, StarCoder-derived heuristics, auto-gen detection */

import { estimateTokens } from "./tokens.js";
import type { ExtractedPair, PipelineConfig } from "../types.js";

export function passesQuality(pair: ExtractedPair, config: PipelineConfig): boolean {
  // Reject empty fields (for instruction formats)
  if (config.format !== "completion" && config.format !== "fim") {
    if (!pair.instruction.trim() && !pair.input.trim()) return false;
    if (!pair.output.trim() && !pair.input.trim()) return false;
  } else {
    // Completion/FIM: input is the content
    if (!pair.input.trim() && !pair.output.trim()) return false;
  }

  // Token bounds on the meaningful content
  const content = [pair.instruction, pair.input, pair.output].filter(Boolean).join(" ");
  const totalTokens = estimateTokens(content);
  if (totalTokens < config.minTokens) return false;
  if (totalTokens > config.maxTokens) return false;

  // StarCoder-derived quality checks on code content
  const codeContent = pair.input || pair.output;
  if (pair.metadata.source === "code" && codeContent) {
    if (!passesCodeQuality(codeContent)) return false;
  }

  // Reject excessive repetition (>50% repeated lines)
  if (hasExcessiveRepetition(codeContent || pair.output)) return false;

  // Reject auto-generated content
  if (isAutoGenerated(codeContent || pair.output)) return false;

  return true;
}

/** StarCoder/CodeParrot-derived code quality checks */
function passesCodeQuality(code: string): boolean {
  const lines = code.split("\n");

  // Max line length < 1000 chars
  if (lines.some((l) => l.length > 1000)) return false;

  // Mean line length < 100 chars
  const meanLength = lines.reduce((sum, l) => sum + l.length, 0) / lines.length;
  if (meanLength > 100) return false;

  // Alphanumeric ratio > 0.25
  const alphanumeric = (code.match(/[a-zA-Z0-9]/g) || []).length;
  if (code.length > 0 && alphanumeric / code.length < 0.25) return false;

  return true;
}

function hasExcessiveRepetition(text: string): boolean {
  const lines = text.split("\n").filter((l) => l.trim());
  if (lines.length < 5) return false;

  const counts = new Map<string, number>();
  for (const line of lines) {
    const trimmed = line.trim();
    counts.set(trimmed, (counts.get(trimmed) || 0) + 1);
  }

  const maxRepeat = Math.max(...counts.values());
  return maxRepeat / lines.length > 0.5;
}

/** Detect auto-generated files by keyword */
function isAutoGenerated(text: string): boolean {
  const firstLines = text.slice(0, 500).toLowerCase();
  const markers = [
    "do not edit",
    "auto-generated",
    "autogenerated",
    "generated by",
    "@generated",
    "this file is generated",
    "machine generated",
    "code generated by",
  ];
  return markers.some((m) => firstLines.includes(m));
}
